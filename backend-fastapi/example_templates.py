def get_examples_for_type(record_type: str, subject: str = None):
    """기록 유형과 과목에 따른 예시 반환"""
    if record_type == "subject" and subject and subject in EXAMPLE_TEMPLATES["subject"]:
        return EXAMPLE_TEMPLATES["subject"][subject]
    elif record_type in EXAMPLE_TEMPLATES:
        return EXAMPLE_TEMPLATES[record_type]
    return []

def format_examples(examples: list, max_examples: int = 2) -> str:
    """예시를 프롬프트에 포함할 수 있는 형식으로 변환"""
    if not examples:
        return ""

    selected_examples = examples[:max_examples]
    formatted = "\n\n참고할 작성 예시:\n"
    for i, example in enumerate(selected_examples, 1):
        formatted += f"\n[예시 {i}]\n{example}\n"

    formatted += "\n위 예시들의 문체, 구성, 표현 방식을 참고하여 작성해주세요.\n"
    return formatted
